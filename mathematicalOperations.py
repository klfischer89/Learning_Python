# mathematicalOperations.py
import math

# Basic arithmetic operations
print(5+4)
print(5-4)
print(5/4)
print(5*4)

print(5**4) # Exponentiation
print(5%4)  # Modulus
print(5//4) # Floor Division

print(round(3.333  , 2)) # Rounds to 2 decimal places
print(math.ceil(3.333))  # Rounds up to the nearest integer
print(math.floor(3.777)) # Rounds down to the nearest integer

print(math.sqrt(16))  # Square root
print(math.pow(2, 3)) # 2 raised to the power of 3
print(math.log(100, 10)) # Logarithm base 10 of 100

